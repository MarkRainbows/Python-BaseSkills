#函数式编程
def text():#text 为函数名
    """注释功能有哪些"""
    print("宣告黎明的露之歌")#函数的功能模块 实现你注释功能的代码模块
    return 0 #并给出返回值

#面向过程  即为 将一段段功能代码包含到def的过程当中 以后方便调用的过程就叫面向过程
def asd():# asd 为函数名
    """注释功能是有哪些"""
    print("宣告黎明的海之歌")#函数的功能模块 实现你注释功能的代码模块
    # 面向过程没有返回值

text()#执行函数里面的代码
asd()#执行函数里面的代码

f = text()#执行函数里面的代码
x = asd()#执行函数里面的代码
print(f)#返回结果
print(x)#返回结果

